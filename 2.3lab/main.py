def Split(A):
    """
    Разделяет список A на два списка:
    B - содержащий все чётные числа из A,
    C - содержащий все нечётные числа из A.

    Параметры:
    A (list of int): исходный список чисел

    Возвращает:
    tuple: (B, C) - два списка с чётными и нечётными числами соответственно
    """
    B = []
    C = []
    for num in A:
        if num % 2 == 0:
            B.append(num)
        else:
            C.append(num)
    return B, C

A = [1, 2, 3, 4, 5, 6, 7, 8, 9]
B, C = Split(A)
print("Чётные числа:", B)
print("Нечётные числа:", C)
